#!/usr/bin/env bash
# worktree.sh — task-scoped git-worktree helpers for PyAutoLabs parallel development.
#
# Source this file from skills, then call the functions directly:
#
#   source PyAutoBrain/bin/worktree.sh
#   worktree_create my-task PyAutoFit PyAutoArray
#   source ~/Code/PyAutoLabs-wt/my-task/activate.sh
#   worktree_remove my-task
#
# Design notes
# ------------
# * PyAutoLabs libraries are imported via hardcoded PYTHONPATH, not editable
#   installs. A worktree under a different path will NOT be picked up unless
#   PYTHONPATH is explicitly overridden. activate.sh does that.
# * One task = one root under $PYAUTO_WT_ROOT. Affected library repos become
#   real git worktrees; every other top-level PyAutoLabs entry is symlinked
#   back to the canonical checkout so relative paths keep working.
# * Numba and matplotlib cache dirs are per-task to prevent concurrent pytest
#   runs from thrashing each other's caches.

set -o pipefail

PYAUTO_MAIN="${PYAUTO_MAIN:-$HOME/Code/PyAutoLabs}"
PYAUTO_WT_ROOT="${PYAUTO_WT_ROOT:-$HOME/Code/PyAutoLabs-wt}"

# Libraries that participate in PYTHONPATH. Order matters — it mirrors the
# order in ~/.bashrc.
PYAUTO_LIBS=(PyAutoConf PyAutoFit PyAutoArray PyAutoGalaxy PyAutoLens)

# _worktree_is_lib repo_name -> 0 if repo is a library, 1 otherwise
_worktree_is_lib() {
  local repo="$1" lib
  for lib in "${PYAUTO_LIBS[@]}"; do
    [[ "$lib" == "$repo" ]] && return 0
  done
  return 1
}

# _worktree_default_branch repo_name -> echoes the default branch name for repo
# All PyAutoLabs repos use `main` as of 2026-04-10.
_worktree_default_branch() {
  echo "main"
}

# worktree_root_path <task-name> -> absolute path to the task's worktree root
worktree_root_path() {
  echo "$PYAUTO_WT_ROOT/$1"
}

# worktree_activate_script <task-name>
# Prints the contents of activate.sh for the given task to stdout.
worktree_activate_script() {
  local task="$1"
  local root
  root="$(worktree_root_path "$task")"
  local lib paths=""
  for lib in "${PYAUTO_LIBS[@]}"; do
    paths+="$root/$lib:"
  done
  paths="${paths%:}"
  cat <<EOF
# activate.sh — source before running any Python in this worktree.
# Generated by PyAutoBrain/bin/worktree.sh for task "$task".
export PYAUTO_TASK="$task"
export PYAUTO_ROOT="$root"
export PYTHONPATH="$paths\${PYTHONPATH:+:\$PYTHONPATH}"
export NUMBA_CACHE_DIR="/tmp/numba_cache_$task"
export MPLCONFIGDIR="/tmp/matplotlib_$task"
mkdir -p "\$NUMBA_CACHE_DIR" "\$MPLCONFIGDIR"
EOF
}

# _worktree_add_one <task-name> <repo>
# Low-level helper: creates (or checks out) a git worktree for <repo> inside
# the task root on branch feature/<task>. Assumes the root directory exists
# and that no real worktree is already present at the destination. Shared by
# worktree_create and worktree_add_repo.
_worktree_add_one() {
  local task="$1" repo="$2"
  local repo_src="$PYAUTO_MAIN/$repo"
  local repo_dest="$PYAUTO_WT_ROOT/$task/$repo"
  local branch="feature/$task"

  if [[ ! -d "$repo_src/.git" ]]; then
    echo "_worktree_add_one: $repo_src is not a git repo" >&2
    return 1
  fi

  local default
  default="$(_worktree_default_branch "$repo")"
  git -C "$repo_src" fetch origin "$default" --quiet || true

  if git -C "$repo_src" show-ref --verify --quiet "refs/heads/$branch"; then
    git -C "$repo_src" worktree add "$repo_dest" "$branch"
  else
    git -C "$repo_src" worktree add -b "$branch" "$repo_dest" "origin/$default"
  fi
}

# worktree_create <task-name> <repo1> [repo2 ...]
# Creates $PYAUTO_WT_ROOT/<task>/ and inside it:
#   - a real git worktree for each listed repo, on branch feature/<task>
#     (or checked out if the branch already exists)
#   - a symlink back to $PYAUTO_MAIN/<name> for every other top-level entry
#   - activate.sh with the per-task PYTHONPATH / cache overrides
# The branch is created from the repo's default branch if it doesn't exist.
# Accepts both library repos (PyAutoFit, etc.) and workspace repos
# (autolens_workspace, etc.).
worktree_create() {
  local task="$1"
  shift
  if [[ -z "$task" || $# -eq 0 ]]; then
    echo "worktree_create: usage: worktree_create <task-name> <repo1> [repo2 ...]" >&2
    return 2
  fi

  local root
  root="$(worktree_root_path "$task")"

  if [[ -e "$root" ]]; then
    echo "worktree_create: $root already exists. Refusing to overwrite." >&2
    return 1
  fi

  mkdir -p "$root" || return 1

  local repo
  for repo in "$@"; do
    if ! _worktree_add_one "$task" "$repo"; then
      rm -rf "$root"
      return 1
    fi
  done

  # Symlink every other top-level entry in PyAutoLabs back to the main checkout
  # so relative paths (admin_jammy/, autolens_workspace/, etc.) keep resolving.
  local entry name
  for entry in "$PYAUTO_MAIN"/*; do
    name="$(basename "$entry")"
    [[ -e "$root/$name" ]] && continue  # already a real worktree
    ln -s "$entry" "$root/$name"
  done

  worktree_activate_script "$task" > "$root/activate.sh"
  chmod +x "$root/activate.sh"

  echo "worktree_create: ready at $root"
  echo "  activate with: source $root/activate.sh"
}

# worktree_add_repo <task-name> <repo>
# Attaches a new repo worktree to an existing task root. Used to bring a
# workspace repo into a task that previously only claimed library repos
# (the library→workspace transition of a "both" task). Replaces the
# placeholder symlink installed by worktree_create with a real worktree on
# feature/<task>. Idempotent: if the repo is already a real worktree, it
# succeeds without doing anything.
#
# activate.sh does NOT need regeneration — its PYTHONPATH covers every
# library path statically, and workspace repos are not on the PYTHONPATH,
# so attaching them as real worktrees is transparent to Python.
worktree_add_repo() {
  local task="$1" repo="$2"
  if [[ -z "$task" || -z "$repo" ]]; then
    echo "worktree_add_repo: usage: worktree_add_repo <task-name> <repo>" >&2
    return 2
  fi

  local root
  root="$(worktree_root_path "$task")"
  if [[ ! -d "$root" ]]; then
    echo "worktree_add_repo: $root does not exist — run worktree_create first" >&2
    return 1
  fi

  local repo_dest="$root/$repo"
  if [[ -e "$repo_dest" && ! -L "$repo_dest" ]]; then
    echo "worktree_add_repo: $repo is already a real worktree at $repo_dest"
    return 0
  fi

  # Replace the placeholder symlink (installed by worktree_create) with a
  # real worktree.
  [[ -L "$repo_dest" ]] && rm "$repo_dest"

  if ! _worktree_add_one "$task" "$repo"; then
    # Best-effort: restore the symlink so the root is not left half-broken.
    ln -s "$PYAUTO_MAIN/$repo" "$repo_dest" 2>/dev/null || true
    return 1
  fi

  echo "worktree_add_repo: added $repo to $root on branch feature/$task"
}

# worktree_remove <task-name>
# Removes every real worktree under the task root, refuses if any is dirty or
# has unpushed commits (unless PYAUTO_WT_FORCE=1), then removes the root and
# its per-task cache dirs.
# worktree_claim_is_stale <task-name> <worktree-root>
# True (0) when PyAutoMind/active.md STILL claims <task-name> and every repo in
# the worktree is on a branch already merged into origin/main — i.e. the work
# is done but the claim was never released. docs/agent_failure_modes.md
# mitigation 3 (F4: twice on 2026-07-16 a completed task's stale claim blocked
# the next one, and the completion record went unwritten).
# Fail-open by design: anything unknown (no active.md, unfetched origin/main,
# squash-merged branch) returns 1 = "not stale" and the caller proceeds.
worktree_claim_is_stale() {
  local task="$1" root="$2"
  local active="$PYAUTO_MAIN/PyAutoMind/active.md"
  [[ -f "$active" ]] || return 1
  grep -Eq "^## ${task}([[:space:]]|\$)" "$active" || return 1

  local entry head saw_merged=0
  for entry in "$root"/*; do
    [[ -L "$entry" ]] && continue
    [[ -e "$entry/.git" ]] || continue
    # Unmerged (or unknowable: squash-merge, unfetched origin) → not stale.
    git -C "$entry" merge-base --is-ancestor HEAD origin/main 2>/dev/null || return 1
    head="$(git -C "$entry" rev-parse HEAD 2>/dev/null)" || return 1
    # An ancestor of origin/main is NOT proof of a merge: a worktree created
    # but never committed to also sits on one (its tip is just an old main
    # commit). A branch that was actually MERGED (--merge / --no-ff) hangs off
    # a merge commit's second parent, so it is absent from origin/main's
    # first-parent chain. That is the discriminator; without it, abandoning a
    # never-committed worktree would be refused — a false positive, and a
    # noisy refusal trains bypass-by-default.
    if git -C "$entry" rev-list --first-parent origin/main 2>/dev/null | grep -qx "$head"; then
      continue          # never diverged from main → nothing to record here
    fi
    saw_merged=1
  done
  [[ "$saw_merged" == "1" ]]
}

worktree_remove() {
  local task="$1"
  if [[ -z "$task" ]]; then
    echo "worktree_remove: usage: worktree_remove <task-name>" >&2
    return 2
  fi

  local root
  root="$(worktree_root_path "$task")"
  if [[ ! -d "$root" ]]; then
    echo "worktree_remove: $root does not exist — nothing to do"
    return 0
  fi

  local entry name
  local dirty_repos=()
  for entry in "$root"/*; do
    [[ -L "$entry" ]] && continue          # symlink, skip
    [[ -e "$entry/.git" ]] || continue     # not a worktree (linked worktrees use a .git FILE, not a dir)
    name="$(basename "$entry")"
    if [[ -n "$(git -C "$entry" status --porcelain)" ]]; then
      dirty_repos+=("$name")
    fi
  done

  if [[ ${#dirty_repos[@]} -gt 0 && "${PYAUTO_WT_FORCE:-0}" != "1" ]]; then
    echo "worktree_remove: refusing to remove — dirty repos: ${dirty_repos[*]}" >&2
    echo "  commit/stash your work, or set PYAUTO_WT_FORCE=1 to override." >&2
    return 1
  fi

  # Stale-claim guard (docs/agent_failure_modes.md mitigation 3). Removing the
  # worktree of a MERGED task whose active.md claim is still registered strands
  # that claim: it blocks the next task on a phantom conflict and the
  # completion record never gets written. Refuse rather than warn — a warning
  # at this exact moment is precisely what failed twice on 2026-07-16.
  if [[ "${PYAUTO_WT_FORCE:-0}" != "1" ]] && worktree_claim_is_stale "$task" "$root"; then
    echo "worktree_remove: refusing to remove — '$task' is merged but PyAutoMind/active.md still claims it." >&2
    echo "  Record the completion and release the claim FIRST:" >&2
    echo "    python3 PyAutoMind/scripts/lifecycle.py record $task --date <YYYY-MM-DD> --from-file <body> [--prompt <file>] --apply" >&2
    echo "    then drop the '## $task' entry from PyAutoMind/active.md and commit (explicit file pathspecs)." >&2
    echo "  Abandoned or unmerged work: set PYAUTO_WT_FORCE=1 to override." >&2
    return 1
  fi

  for entry in "$root"/*; do
    [[ -L "$entry" ]] && continue
    [[ -e "$entry/.git" ]] || continue     # linked worktrees have .git as a FILE
    name="$(basename "$entry")"
    git -C "$PYAUTO_MAIN/$name" worktree remove --force "$entry" || {
      echo "worktree_remove: failed to remove worktree for $name" >&2
      return 1
    }
    # Belt-and-braces: prune stale metadata in case `remove` left a "prunable"
    # entry behind (seen on some git versions).
    git -C "$PYAUTO_MAIN/$name" worktree prune
  done

  # Unlink the remaining symlinks + activate.sh, then rmdir.
  find "$root" -maxdepth 1 -mindepth 1 -exec rm -rf {} +
  rmdir "$root" 2>/dev/null || rm -rf "$root"

  rm -rf "/tmp/numba_cache_$task" "/tmp/matplotlib_$task"

  echo "worktree_remove: removed $root"
}

# worktree_list_claimed
# Parses PyAutoMind/active.md and emits one line per claimed repo:
#   <task>\t<repo>\t<branch>\t<worktree-path>
# Used by conflict-guard steps. A task is "claimed" if its entry has a
# `worktree:` field; legacy non-worktree tasks are emitted with worktree-path
# set to the literal string "-".
worktree_list_claimed() {
  local active="$PYAUTO_MAIN/PyAutoMind/active.md"
  # Back-compat: fall back to the pre-rename PyAutoPrompt/ path if present.
  [[ -f "$active" ]] || active="$PYAUTO_MAIN/PyAutoPrompt/active.md"
  if [[ ! -f "$active" ]]; then
    return 0
  fi
  awk '
    /^## / {
      task = $2
      wt = "-"
      next
    }
    /^- worktree:/ {
      wt = $3
      next
    }
    /^  - [A-Za-z]/ {
      # Matches lines like "  - PyAutoFit: feature/foo"
      gsub(/^  - /, "")
      split($0, parts, ": ")
      repo = parts[1]
      branch = parts[2]
      printf "%s\t%s\t%s\t%s\n", task, repo, branch, wt
    }
  ' "$active"
}

# worktree_check_conflict <task-name> <repo1> [repo2 ...]
# Exits 0 if none of the requested repos are claimed by a different task.
# Exits 1 and prints the conflicts to stderr otherwise.
worktree_check_conflict() {
  local task="$1"
  shift
  local want repo existing_task existing_repo existing_branch existing_wt rc=0
  for want in "$@"; do
    while IFS=$'\t' read -r existing_task existing_repo existing_branch existing_wt; do
      if [[ "$existing_repo" == "$want" && "$existing_task" != "$task" ]]; then
        echo "CONFLICT: $existing_repo is claimed by task '$existing_task'" >&2
        echo "  branch:   $existing_branch" >&2
        echo "  worktree: $existing_wt" >&2
        rc=1
      fi
    done < <(worktree_list_claimed)
  done
  return $rc
}
